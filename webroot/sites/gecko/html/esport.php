                    <p>L’e-sport ne vous dit peut-être pas grand-chose mais c’est bel et bien le dernier sport à la mode de plus en plus populaire. Hé oui ! Le sport « électronique » se fait enfin une place dans notre monde.</p>

                    <p class="important"> <span class="ligne_important"></span>L’e-sport existe depuis longtemps et il a commencé dès le premier tournoi de space invader (il y a déjà plus de 35 ans !) et est maintenant en plein essor. Comment cela a pu arriver ?</p>                    
                    <p>Au début, les « lans » (compétitions où l’on devait apporter son ordinateur) n’intéressent pas grand monde. Ce n’était que des petits tournois composés de très peu de personnes où même les « cash prizes » (récompense en argent ou en matériel informatique) n’étaient pas très élevés et ils étaient souvent organisés dans des gymnases ou des cyber cafés. En moyenne, les joueurs gagnaient entre 50 et 700 euros (une fois les coûts de déplacement payés) par personne avec environ 100 euros de matériel payé par les sponsors.
                    
                    <br/><br><img alt="image e-sport" src="../img/articles/esport/esport2.jpg"/><br/><br>
                    Peu à peu, les tournois se sont démocratisés mais ils ont vraiment explosé lorsque le plus gros premier tournoi sur le jeu-vidéo de stratégie « dota2 » est arrivé avec un cash prize de 1,6 million de dollars pour la première place ! Ce fut le plus gros tournoi du monde avec le plus gros cash prize jamais enregistré, et beaucoup ont cru à une énorme blague de la part de Steam (développeur du jeu). Pour l’évènement, une salle gigantesque à Seattle fut louée pour l’organisation, plusieurs écrans diffusaient le match et plusieurs équipes de streamers les diffusaient aussi en live sur internet et cela dans plusieurs langues.
                    De multiples équipes sont même venues de Chine pour se disputer la coupe du monde et se fut l’équipe Ukrainienne Natus Vincere qui gagna le championnat.
                    C’est à ce moment-là que l’e-sport à réellement commencé à exister.
                    
                    <br/><br><img alt="image e-sport" src="../img/articles/esport/esport4.jpg"/><br/><br>
                    Les jeux aujourd’hui les plus joués sont League of Legend, Dota2 et Counter strike Global offensive. Et c’est une véritable entreprise ! Les joueurs peuvent être payés jusqu’à 4 000 euros par mois pour disputer les tournois. Ils sont parfois même nourris et logés dans des « gaming house », endroit où l’équipe vit et joue en permanence.  La plupart sont aussi en partenariat avec des marques informatiques ou encore comme pour le cas des ENVYUS (une équipe française connue dans le jeu Counter Strike : global offensive) 
                    sponsorisés par la marque de boisson Monster qu’ils n’hésitent pas à montrer lors de tournois ou même de shootings photo ! 
                    Actuellement, l’e-sport devient une énorme entreprise qui croît de jours en jours et accroît son chiffre d’affaire au fil du temps et devrait engranger 465 millions d’euros d’ici 2017, de quoi faire tourner les têtes.
                    </p>
                    <p>Alors maintenant que vous êtes prévenus, prenez vos manettes, votre souris, votre casque et lancez-vous dans cette aventure !</p>
                    
