                <article><!------------------------------------------------------------------A CHANGER-->
                    <p>Nous avons tous des idées reçues, des préjugés sur les us et coutumes de ceux qui nous entourent. Aujourd’hui, nous nous sommes intéressés au point de vue des jeunes sur :
                    « La religion catholique ».
                    </p>
                    
                    <p class="important"> <span class="ligne_important"></span>Sondage : « Quels sont vos avis, idées reçues et clichés sur la religion catholique ? »</p>
                    
                    <p>En 2008, les stéréotypes étaient nombreux, et certains sont encore dans nos esprits aujourd’hui :</p>
                    <ul>
                        <li>« La messe du dimanche matin (obligation impérative et barbante à respecter à la lettre) »</li>
                        <li>« Les catholiques sont des gens « coincés » qui « ont (malgré cela) beaucoup d’enfants ».</li>
                        <li>« Ils sont contre « tout » (l’homosexualité, le préservatif, l’avortement et les jeux vidéos) ». L’idée de</li>
                        <li>Le prêtre pédophile.</li>
                        <li>«J'ai un écran "catholique 20". Je ne reçois que FR2 pour la messe du matin»</li>
                        <li>«Les enfants vont forcément aux scouts.»</li>
                        <li>«Les catholiques votent à droite»</li>
                        <li>«Ils sont convaincus qu'ils ont la vérité et qu'ils ont raison, du coup ils n'ont plus besoin de réfléchir.»</li>
                        <li>«Ils vont au paradis quand ils meurent.»</li>
                        <li>«Ils font des bonnes actions juste pour aller au paradis ("Ils sont hypocrites").»</li>
                    </ul>
                    
                    <p>Mais bien que certains clichés soient encore présents aujourd’hui, on peut constater une évolution des mentalités et des avis. En effet, avec les nombreuses guerres et les attentats, le mot « religion » a souvent été prononcé, pour toutes sortes de raisons (pas forcément pûrement et simplement religieuses).</p><br/>
                    
                    <p>« Personnellement, je n’en pense rien, je m'en fous, je ne croirais en Dieu que si j'ai une preuve suffisante qui prouve son existence […]. Je n'ai pas de cliché, de préjugé ou quoi, ils font ce qu'ils veulent, un point c’est tout » m’a répondu Pierre lors de mon sondage. 
                    Puis il a ajouté une anecdote : il avait dû accompagner son frère au cathé une fois : « bah, je n’aime toujours pas cet aspect... "Nous vénérons un pauvre gars qu'on a inventé, et nous faisons toute notre vie l'inverse de ce qu'il nous a enseigné".
                    L'Homme dans toute sa splendeur, vénérant des dieux qui n'ordonnent que la paix, l'amour et la compassion ; mais quelle que soit la religion, ils font la guerre en leurs noms ; stupide ». 
                    </p><br/>
                                        
                    <p>Suite à la réponse et au partage de Pierre, Aurélien donne son avis : « Personnellement je n'en pense pas grand chose non plus. Je ne suis pas croyant, mais si les gens croient ça ne me gène pas. Par contre ça va commencer à me déranger si la religion prend une place trop importante en politique par exemple. De plus la religion est parfois un argument, injustifié, pour créer des conflits dans le monde.
                    Je [veux] dire que dans certains pays, les dirigeants ou le gouvernement sont fortement influencés par la religion en général (et cela n’est pas vraiment une bonne chose).
                    </p>
                    
                    <p>Bien que l’on ait pas d’avis dans un premier temps, on retrouve quand même l’idée de l’individualité, qui ne juge pas l’autre tant qu’il n’empiète pas « sur son espace vital ».<br/>
                    Marine à ce sujet m’a répondu qu’elle n’avait pas de préjugés ou d’idées reçues, car elle ne juge pas les croyants par tolérance et par respect de l’autre.    
                    </p>
                    
                    <p>Hélèna, quant à elle, a soulevé une évolution de l’image des catholiques très intéressante : les paroles et le comportement du « Nouveau Pape ».
                    « Bah moi je n'ai pas grand chose à dire sur le sujet. Je trouve qu'on avait une assez mauvaise image de la religion catholique (ennui à la messe le dimanche matin etc ... ) mais maintenant ça a un peu changé sur certains points grâce au nouveau pape (plus proche des gens, ouvert sur les préoccupations de maintenant), il a su se faire connaître et apprécier dans le monde entier (et pas seulement par les croyants).
                    J'ai plutôt un avis favorable parce que je trouve que souvent les gens savent faire la part des choses entre leur religion et les gens non croyants (en tout cas pour la religion catholique). »
                    </p>
                    
                    <p>Pour ce qui est de la pédophilie chez les prêtres, il ne faut pas en faire une généralité : il y en a (film Spotlight), mais très peu. Les prêtres sont des êtres humains comme les autres, ils ne sont pas parfaits. Et juger que tous les prêtres sont pédophiles revient à dire, par exemple, que tous les étrangers sont dangereux parce que quelques uns ont fait preuve de violence.
                    Un prêtre a été accusé à tort, parce que des gens font des généralités et voient le mal partout.
                    </p>
                    
                    <p>Malgré les préjugés et les mauvais côtés de l’être humain (qui sont davantage retenus), la majorité des jeunes gardent un esprit ouvert et tolérant.
                    La religion ne doit pas être un sujet tabou, qui effraie, ou nous met sur la défensive. Mais une différence qui doit pouvoir être partagée, comme on partage sa passion pour la moto, la musique, la cuisine ou son plaisir de collectionner des timbres, des billes, des cartes ou son goût pour les jeux vidéo, les bons films ou les objets anciens.
                    </p><br/>
                    
                    <p>Pour clore cet article sur la religion catholique, je vous propose d’aller visionner quelques vidéos du « Cathologue », afin de vous permettre de connaître une religion avec humour ;-).</p><br/><br/>
                    
                    
