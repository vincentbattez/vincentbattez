                    <p>Bonjour à tous ! Bienvenue dans la rubrique Star Wars que vous soyez néophytes ou maître jedi.
                    Pour ce premier opus nous allons nous intéresser aux films de la célèbre saga.
                    Comme vous le savez déjà, l’aventure de George Lucas a débuté sur nos écrans en 1977 avec le quatrième épisode de la saga « Un nouvel espoir ».
                    Suite au succès fulgurant de ce premier film, l’aventure de nos jeunes héros s’est poursuivie à travers deux nouveaux films respectivement « L’empire contre-attaque » et « Le retour du jedi ».
                    Mais l’aventure devait originellement s’arrêter ici…
                    A la surprise générale, c’est en 1999 que sort l’épisode 1 « La menace fantôme » qui relate les faits précédant la première trilogie. S’en suit donc une seconde trilogie jusqu’en 2005, trilogie qui a vu naître probablement le méchant le plus connu du cinéma, nous parlons bien sûr de Dark Vador.
                    Malheureusement, George Lucas décida à la suite de ces deux trilogies de ne pas continuer l’aventure…
                    </p>
                    
                    <p class="important"> <span class="ligne_important"></span>Malheureusement, George Lucas décida à la suite de ces deux trilogies de ne pas continuer l’aventure…</p>
                    
                    <p>Le désormais célèbre réalisateur décida même de revendre son studio « Lucasfilm » à Disney, qui créera une grosse polémique au sein des fans de la saga. Malgré tout, George Lucas accepta et Disney se mit sans plus attendre à imaginer une troisième trilogie.
                    Trilogie désormais en route puisque récemment l’épisode 7 intitulé « Le réveil de la Force » sortait dans nos cinémas en décembre dernier. On retrouve encore une fois trois nouveaux héros et un nouveau méchant à combattre Kylo Ren. L’épisode 7 fut le plus gros succès de 2015 puisqu’il a battu plusieurs records dès sa première semaine de projection. Disney a déjà annoncé un film Star Wars par an. Le prochain « Star Wars : Rogue One » sera un préquel à la trilogie de 1977, on apprendra comment la princesse Leïa a obtenu les plans de l’Étoile Noire.
                    </p>
                    
                    <p>En 2017, sortira l’épisode 8, le deuxième film de la troisième trilogie qui racontera la suite de l’épisode 7 fraîchement sorti.<br/>
                    En 2018, nous aurons droit à un spin-off sur l’histoire et le passé du célèbre contrebandier Han Solo. En 2019, la trilogie aboutira avec la sortie de l’épisode 9, et on pourrait imaginer le combat final entre Rey et Kylo Ren.<br/>                    
                    Et le dernier film Star Wars prévu pour l’instant est un autre spin-off sur le personnage adoré des fans, Boba Fett, un chasseur de primes, qui a su conquérir le cœur des fans grâce à son armure unique et aussi par ses débuts de chasseur de primes visible dans l’épisode 2 « L’attaque des clones ».</p><br/>
                    
                    <p>Pour en conclure avec notre épopée galactique, nous vous proposons un top 10 de quelques chiffres sur l’univers Star Wars.</p><br/>
                    
                    <ul>
                        <li>66. C’est la taille de maître Yoda en cm.</li>
                        <li>7000. C’est le taux minimum de midichloriens nécessaires pour devenir jedi. Notons que le sith Comte Dooku (Dark Tyrannus pour les intimes) n’en possède que 3500.</li>
                        <li>27 700. C’est le taux de midichloriens présents chez Anakin Skywalker, ce qui en fait le jedi qui en possède le plus à notre connaissance, dépassant même Dark Sidious (20 500) et Maître Yoda (17 700).</li>
                        <li>400. C’est l’espérance de vie d’un Wookie (Chewbacca).</li>
                        <li>14. C’est le nombre de fois que la célèbre phrase « Que la Force soit avec vous » est prononcée à travers les deux premières trilogies.</li>
                        <li>366. C’est le nombre d’heures nécessaires pour faire le trajet Paris-Lune à bord du Faucon Millénium sans générateur hyperdrive.</li>
