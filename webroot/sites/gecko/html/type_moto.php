                        <ul>
                            <li>Vous êtes quelqu’un qui aime rouler par tout temps ? Vous aimez faire de longue route ou partir en week-end avec votre compagne ou vos amis sur votre moto ? Vous désirez une position de conduite droite et voir la route de haut ? Alors je vous conseille des motos du type TRAIL.</li>
                            <li>Si vous êtes quelqu’un qui aime les sensations sans être « allongé » sur votre moto, si vous aimez faire de la route pour aller en week-end mais pas trop loin (environs 200 km) et si vous aimez avoir une moto maniable en ville mais aussi être à l’aise sur l’autoroute, je vous conseille des motos de type ROADSTER, qui sont des moto très accessibles au niveau du prix. Votre moto sera votre passe-partout aussi bien en ville que sur l’autoroute.</li>
                            <li>Et enfin si vous êtes quelqu’un en recherche de sensations fortes, vous aimez le goût du risque et pourquoi pas pouvoir faire de la piste le week-end avec votre moto de tous les jours, la sportive et certainement la moto qu’il vous faut. Celle-ci vous emmènera peut-être sur les autoroutes en Allemagne ou les vitesses ne sont pas limitées ou sur un circuit spécialisé pour la course moto. Ce genre de 2 roues n’est pas vraiment fait pour la ville.</li>
                        </ul>
