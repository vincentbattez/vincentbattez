                <article><!------------------------------------------------------------------A CHANGER-->
                    
                    <p>Aujourd’hui, nous allons nous attaquer à une chose que vous connaissez tous et que vous avez sûrement tous chez vous, un ordinateur ! Vous seriez environ 24.5 millions de foyers à en posséder un selon de récentes études.
                    </p>
                    <p class="important"> <span class="ligne_important"></span>Et donc comment bien choisir un ordinateur ? C'est toujours une question problématique.</p>
                    
                    <p>Nous voulons toujours quelque chose de correct et en avoir pour son argent mais malheureusement même si une fois cela respecté, le produit n’est pas en accord avec l’utilisation que l’on en fait. C’est donc aujourd’hui que nous avons décidé de combattre ce problème !<br/>
                        Par ailleurs un petit cours rapide s’impose :<br/><br/>
                        Nous avons dans un ordinateur :
                        <ul>
                            <li>un processeur (grosse calculatrice)</li>
                            <li>une carte graphique (s’occupe de la 3D/vidéo)</li>
                            <li>une/des barrette(s) de ram (stock des données temporaires)</li>
                            <li>un disque dur (vos données)</li>
                            <li>une carte mère (connecte tout ensemble)</li>
                        </ul>
                    </p>
                <p>
                    Il y a plusieurs familles dans chacun de ces composants.<br/>
                    Là où nous pouvons voir les plus grosses familles sont dans les processeurs : la marque AMD qui elle est une marque accessible pour toutes les bourses, et la marque concurrente Intel qui elle reste leader sur le marché grâce à son gage de qualité et de performance.
                    <br/><br/>
                    Et les cartes graphiques : AMD, moins chère que la marque leader et NVIDIA qui quant à elle, elle garantit une image de qualité et des performances supérieures à toutes les autres cartes disponibles sur le marché.
                    <br/><br/>
                    Maintenant que nous avons parlé composant, parlons ordinateur en eux-mêmes.
                </p>
                
                    <p class="important"> <span class="ligne_important"></span>Nous avons ici 2 familles qui se battent depuis des années : les ordinateurs Apple et les Ordinateur Windows.</p>
                
                <p>Démarrons par les ordinateurs de la marque Apple. La marque possède son propre design et son propre système d’exploitation. Elle est souvent considérée comme une marque de luxe ou destinée aux professionnels. Elle n’est donc pas accessible a toutes les bourses. Hé oui car là ou un mac coûterait 2000 euros, un pc avec les mêmes composants coûterait 1150 euros. Pourquoi une aussi grosse différence de prix ? Les ordinateurs Mac ont un gage de qualité, les écrans sont de meilleure qualité (écran retina possédant beaucoup plus de pixels). Il y a aussi le fait que les ordinateurs Apple sont quasiment impiratables, le système d’exploitation est tellement sécurisé qu’avoir un virus sur un Mac est quasi impossible. Par ailleurs les composants, même s’ils sont identiques à un pc Windows tiendront mieux dans le temps car mieux optimisés par le système d’exploitation.<br/>
                Les ordinateurs Apple se prennent donc au final une marge sur chaque composant mais
                cela reste justifié. Il faudra noter que Mac est surtout fait pour travailler et non pour jouer
                ou encore juste regarder des vidéos dessus.<br/><br/>
                Un ordinateur sous Windows est beaucoup plus accessible quant à lui. Windows a dû choisir
                comme politique d’être accessible et de pouvoir permettre à tout le monde de pouvoir
                s’offrir un ordinateur peu importe les moyens de chacun.<br/><br/>
                Les seuls problèmes sont les constructeurs ! Certains n’hésitent pas à mettre des composants
                de mauvaise qualité afin que le pc ne tienne pas longtemps (on parle d’obsolescence
                programmée), ou encore encrypter des pièces afin que nous ne puissions pas réparer
                l’ordinateur. Par exemple, la marque HP a modifié le disque dur afin qu’on ne puisse pas le
                changer.<br/>Par ailleurs, un ordinateur sous Windows pourra être modifié dans le temps (selon le
                budget de départ et s’il est fixe). On pourra ainsi changer les barrettes de ram ou encore le
                disque dur comparé à Apple qui eux ont décidé de souder le disque dur à la carte mère,
                rendant donc l’ordinateur irréparable et impossible à faire évoluer.<br/><br/>
                                    Bon, hé bien maintenant que nous avons fait les comparaisons nous pouvons affilier un pc à
                chacun d’entre vous.
                </p>
                
                    <p class="important"> <span class="ligne_important"></span>Un gamer</p>
                
                <p>Le pc gamer a besoin d’être puissant, avoir un très bon processeur et une très
                bonne carte graphique pour faire tourner tous les derniers jeux en HD. Le pc gamer d’entrée
                de gamme se situe aux environs de 500-700 euros car il aura besoin d’évoluer (nouvelle
                carte graphique par exemple). Le mieux est donc d’acheter un pc gamer fixe car un
                ordinateur portable sera plus cher pour l’équivalent d’un pc fixe et ne pourra pas évoluer.
                La plupart des gamers font monter leur pc en boutique mais certains ne voulant pas
                s’embêter achètent des pc gamer déjà fait, comme le marque ASUS ROG, GYGABYTE ou
                encore MSI. Jouer sur pc revient moins cher qu’une console car même si l’achat est coûteux
                au début, vous n’aurez qu’à changer la carte graphique (150-200€) comparé à une
                nouvelle console qui coûterait 300 euros. A noter : jouer en ligne sur un pc est gratuit !</p>
                
                    <p class="important"> <span class="ligne_important"></span>Un professeur</p>
                <p>Le professeur a besoin de se déplacer, d’avoir tous ses fichiers avec lui. Par
                ailleurs le prof’ aura plusieurs clefs USB d’élèves qui viendront se brancher à son ordinateur,
                pouvant donc apporter pas mal de virus ! Pour cela, un pc portable de type Notebook
                suffira, car ils sont souvent petits, légers et rentrent facilement dans un sac. Mais si la peur
                du virus et la peur de la panne vous inquiètent, partez donc sur un ordinateur portable mac,
                il vous faudra compter entre 1000 et 2500 euros.</p>
                
                    <p class="important"> <span class="ligne_important"></span>Ado</p>
                <p>Facebook, Youtube et faire des Powerpoint pour vos projets vous suffit ? Alors partez
                sur un pc portable, vous pourrez ainsi le mettre dans votre lit et passer de longues heures
                dessus. En ce qui concerne les composants, il vaut mieux rester sur un Inter core I3 sans carte
                graphique car le processeur fera très bien le travail, sinon vous pouvez optez pour un pc
                portable avec carte graphique si vous voulez jouer un peu dessus, dans ce cas une gt750 ou
                même une gtx fera l’affaire. Un pc de marque Acer ou MSI conviendra très bien. Prévoyez
                minimum un budget de 200€ pour un notebook et 300€ pour un pc portable.</p>
                
                    <p class="important"> <span class="ligne_important"></span>Etudiant</p>
                <p>Cela dépend de vos études ! Si vous cherchez un ordinateur très compact juste
                pour aller sur Word et taper vos cours dans un amphi, restez sur un notebook (200 euros
                cher Asus), de plus regardez bien certains notebooks 2 en 1 car la tablette possède une
                batterie et le clavier aussi (contrairement à la surface pro de Windows) ce qui vous fait 2 batteries et vous permettra donc de ne jamais tomber en panne lors du cours de Mr. Pâté prof de zoologie astronomique.<br/>
                Si vous faites des études dans le graphisme un notebook ne vous suffira pas car pas assez performant. Orientez-vous sur un pc portable minimum Intel core i3 avec une carte graphique de plutôt bonne qualité voir gamer comme une GTX950 (environ 500 euros cher Asus et 1200 cher Apple)
                </p>
                
                    <p class="important"> <span class="ligne_important"></span>Le pc familial</p>
                <p>Pour un petit pc familial mieux vaut éviter le pc portable qui sera transporté partout, qui tombera etc. Surtout qu’un ordinateur familial a besoin de plusieurs sessions afin que chaque personne retrouve ses fichiers. Un ordinateur familial de bonne qualité peut se trouver pour 150-300 euros avec un processeur AMD et une carte graphique bureautique. Bien sûr, il faut éviter certaines marques comme HP, Toshiba ou encore Packard Bell à cause de leur obsolescence programmée.</p>
                
                <p>En conclusion, nous pouvons voir qu’un ordinateur ne doit pas se choisir selon sa beauté, son design ou son prix mais par l’utilité que vous comptez en faire.</p>
                
                
                
                    
