                    <p class="important"> <span class="ligne_important"></span>Le bien être :</p>
                    
                    <p>« un esprit sain dans un corps sain ». Il est plus facile d’être bien dans sa tête lorsque l’on est bien dans son corps. Mais pour être bien dans son corps il faut que la tête suive : un esprit positif et de la motivation sont de bonnes résolutions.</p>
                    
                    <p>Dans la rubrique « Bien être » Nous vous donnerons des petits conseils et des exercices, pour se sentir bien dans son corps (alimentation, détente et exercices physiques). </p>
                    
                    <p>Pour cela, nous avons invité le coach Élise qui vous donnera des exercices réalisables chez vous. Nous vous donnerons également un conseil alimentaire, ainsi que de petits plus « soin de peau » et/ou détente, à chaque parution d’article.</p>
                    
                    <p class="important"> <span class="ligne_important"></span>Conseil alimentaire du jour :</p>
                    
                    <p>« Moment pour manger »
                        Il est conseillé d’éviter de manger avant d’aller au lit. Car quand le corps est inactif, les excès de calories sont stockés sous forme de graisse. Pour éviter cela, si vous avez l’habitude d’aller au lit vers 22h, ne mangez plus après 20h. S’il est impossible pour vous d’aller au lit sans manger, privilégier plutôt des légumes et buvez beaucoup d’eau (car elle permet de couper la sensation de faim).
                    </p>
                    
                    <p class="important"> <span class="ligne_important"></span>Petits plus détente :</p>
                    
                    <p>Pour vous détendre après une longue journée, ou après un gros coup de stress, prenez 5 à 10 min pour vous poser. Asseyez vous, sur une chaise, sur votre lit, dans votre canapé, dans un endroit où vous êtes à l’aise et où vous pouvez être au calme. Puis concentrez-vous sur votre respiration. Prenez le temps de réaliser le gonflement de votre ventre lorsque vous inspirez. Puis expirez, lentement, tranquillement. Restez ainsi, 5 à 10 min à prendre conscience de votre respiration. Cela vous permettra, non seulement de souffler, mais aussi de vous calmer, de vous détendre. Afin de repartir un peu plus serein ;-)</p> <br/><br/>
                    
                    <!--<iframe id="video" src="https://www.youtube.com/embed/Apc-3HmbkDI" frameborder="0" allowfullscreen></iframe><br/><br/>--><!------------------------------------------------------------------A CHANGER-->
