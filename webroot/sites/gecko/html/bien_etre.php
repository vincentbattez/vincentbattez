                    <p class="important"> <span class="ligne_important"></span>Récapitulatif des exercices proposés par notre coach :</p>
                    
                    <p><br/><u>Échauffement :</u> 10 min de vélo d’appartement.<br/>
                        <u>Entrainement :</u><br/>
                        <u><b><br/>1ère partie :</b></u><br/>
                        <ul>
                            <li><u>Exercice 1 :</u>Travailler la taille. S’allonger sur le dos, décoller la tête et les épaules du sol. Tendre les bras, avancer le bras droit vers la cheville droite, puis avancer le bras gauche vers la cheville gauche. Répéter cette alternance.
                            </li>

                            <li><u>Exercice 2 :</u>Gainage grimpeur. Se mettre en position de gainage sur les mains. Alterner la montée du genou droit et du genou gauche (de manière lente ou rapide selon le niveau). Répéter 15 à 20 fois.
                            </li>

                            <li><u>Exercice 3 :</u>Gainage sur les avant-bras. Basculer le bassin de gauche à droite, tout en maintenant la position de gainage.
                            </li>

                            <li><u>Exercice 4 :</u>S’asseoir par terre. Prendre un poids (ou qqch de lourd). Se pencher légèrement en arrière, et faire passer le poids de droite à gauche, tout en le posant bien de chaque côté, afin d’effectuer un mouvement de rotation de la taille. Répéter 15 à 20 fois.
                            </li>

                            <li><u>Exercice 5 :</u>Gainage de côté, en levant un bras ou une jambe (selon ses capacités et le niveau de difficulté souhaité). Bouger le bassin de haut en bas. Répéter 15 à 20 fois. (Puis refaire le même mouvement, 15 à 20 fois, mais de l’autre côté).
                            </li>
                        </ul>
                    </p>
                <p>
                    <u><b><br/>2e partie :</b></u><br/>
                    <ul>
                        <li><u>Exercice 1 :</u>Squat élévation. Faire un squat, lever la jambe droite. Puis, de nouveau un squat, et lever la jambe gauche. Répéter cet enchainement 30 fois.
                        </li>

                        <li><u>Exercice 2 :</u>Fentes. Répéter 20 fois.</li>

                        <li><u>Exercice 3 :</u>Gainage (travailler les fessiers). Se mettre en position gainage. Alterner le lever de la jambe droite et de la jambe gauche. Répéter 20 fois.</li>

                        <li><u>Exercice 4 :</u>Abdos crush. S’allonger. Mettre les jambes en l’air en V. Puis travailler les abdos en passant entre les jambes. Répéter 30 fois.</li>

                        <li><u>Exercice 5 :</u>S’allonger. Passer les jambes de droite à gauche (mouvement de rotation). Répéter 30 fois.</li>

                        <li><u>Exercice 6 :</u>Gainage Jump in Jack. Se mettre en position gainage, et écarter et rassembler les jambes activement. Répéter 45 fois.</li>
                    </ul>
                </p>
            
            <p>
                <u><br/>Étirements</u> : Étirer le dos. Étirer les jambes. <br/>
                <u>Yoga</u> : position de l’enfant (à la fois étirement et détente). Relâcher le dos.
            </p><br/><br/>
            
            <p>Ce programme peut peut-être vous sembler long, mais pas de panique ! Vous n’êtes pas obligé de le suivre à la lettre. Choisissez les exercices en fonctions de vos capacités, de vos attentes et besoins, et de votre temps. 
            De plus, il n’est pas nécessaire de faire une séance de sport tous les jours : 1 à 2 fois par semaine, c’est suffisant et meilleur pour l’organisme.<br/>
