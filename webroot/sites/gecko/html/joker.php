                    <p>Salut voyageur ! Bienvenue dans mon auberge, tu trouveras ici de quoi abreuver ta
                    soif de théorie. Hé oui on va parler de théories mais pas n'importe quelles théories. Des
                    théories sur les origines du plus mystérieux mais pas moins préféré Clown du Crime :
                    le Joker.
                    </p>
                    
                    <p>Apparu dans <strong>Batman#1</strong> au printemps 1940 créé par <strong>Bill
                    Finger, Bob Kane et Jerry Robinson</strong> qui se contestent la
                    création, le Joker est un personnage au <strong>passé trouble</strong> qui nous
                    reste inconnu jusqu'alors. Qu'est ce que j'aime les hommes
                    mystérieux ! Au cours des 7 dernières décennies notre taré nous a
                    révélé <strong>plusieurs versions</strong> de son passé, comme il le dit dans le jeu
                    Batman Arkham City : <em style="color:#808096">« J'aime rendre les choses intéressantes. Un
                    grand sage m'a dit un jour que lorsqu'on avait une histoire il fallait
                    faire en sorte qu'elle soit a choix multiples. »</em></p>
                    
                    <iframe id="video" src="https://www.youtube.com/embed/_yDEOdK0EFg" frameborder="0" allowfullscreen></iframe>
                    
                    <p class="important"> <span class="ligne_important"></span>Théorie 1 : L'usine chimique</p>
                    
                    <p>Tu Tu Tu... Musique de suspense, hé oui c'est
                    la première théorie ! Toi, derrière ton
                    écran, tu vas me dire que ce n'est pas une théorie
                    et tu as raison mais je la classe au rang de
                    théorie car nous ne sommes pas sûr de la
                    véracité de cette version sans rajouter le reb0ot
                    New 52 qui modifie les origines
                    des personnages. Autrefois humoriste raté, l'homme qui deviendra par la suite le Joker n'a pour
                    seul but que de subvenir aux besoin de sa femme enceinte. Mais celle ci meurt peu avant
                    le cambriolage. Il n'a d'autre choix que participer au cambriolage de son ancien lieu de
                    travail : Ace's Chimical. Forcé par les 2 malfrats qui l'accompagnent il revêt le costume
                    de Red Hood pour servir de bouc émissaire, réduisant par la même occasion son champ
                    de vision. Le casse est interrompue par … Superman ?... Green Lantern ?... Et non c'est bien
                    sûr Batman mais lors de l'affrontement « Red Hood » trébuche et tombe dans la cuve
                    d'acide. L'inconnu survit, sa peau a blanchi, ses cheveux ont verdi et un sourire se fige
                    sur son visage. Le Joker est né.</p><br/>
                    
                    <p class="important"> <span class="ligne_important"></span>Théorie 2 : Le Joker est un virus</p>
                    
                    <p>Quoi?! Un virus ? Hé oui, notre psychopathe ne serait pas une personne
                    mais un virus. Un virus qui transforme la personne infectée en
                    clown fou et meurtrier. Ce qui veut dire qu'il n y a pas un mais
                    plusieurs Joker. Cool quand on y pense, le Joker est immortel
                    dans un sens ou peut-être pas. Réponse dans l'épisode suivant.
                    Tout cela expliquerait entre autre l'origine non définie de notre
                    personnage ou les différentes origines car le Joker n'est pas une
                    mais plusieurs personnes.</p><br/>
                    
                    <p class="important"> <span class="ligne_important"></span>Théorie 3 : Le Joker n'est pas une personne</p>
                    
                    <p>Pour cette
                    partie 3 je ne vais pas vous parler d'une mais de deux théories.
                    Promotion du jour ! Pour commencer le Joker ne serait qu'une
                    illusion de Batman, sujet abordé dans le dernier jeu en date :
                    « Batman Arkham Knight ». Voila c'est tout pour la première, il
                    n y a rien d'autre à dire. Déçu ? Hein ? Passons à la suivante. Le
                    Joker serait un être surnaturel car il représente depuis toujours la
                    folie et le chaos absolu. J'aime beaucoup cette idée. Voilà, je voulais juste donner mon
                    avis. Technique de comblage !</p><br/>
                    
                    <p class="important"> <span class="ligne_important"></span>Théorie 4 : Le Joker est immortel</p>
                    
                    <p>Je sais que ca paraît fou, un peu
                    comme le Joker, mais l’explication est cool. Alors reste assis avec tes
                    petits camarades. Notre clown existerait depuis la création de Gotham
                    et ne serait que Cyrus Pinkney. Mr Pinkney
                    ou Cyrus pour les intimes n'est autre que l'architecte de Gotham et qui
                    selon cette même théorie aurait construit Gotham pour déployer sa
                    folie. Tout ça est étayé par des preuves découvertes par notre cher Gordan montrant que
                    le Joker existait bien avant Gotham.</p><br/>

                    
                    <p class="important"> <span class="ligne_important"></span>Théorie 5 : Le Joker est le frère de Batman</p>
                    
                    <p>On finit en beauté avec une théorie des plus sympathiques. Le Joker
                    est Thomas Wayne Junior le grand frère de Batman. Théorie
                    des plus intéressantes car le combat Batman Joker se
                    transformera en combat fratricide. Non je ne parle pas de
                    Games Of Thrones. Thomas a quitté le manoir Wayne peu après
                    la naissance de Bruce, suite à une dispute avec Thomas et
                    Martha, ses deux parents dans cet univers. Oui, tu as bien
                    entendu car dans un autre univers il est devenu Owlman. Nous
                    n'avons plus entendu parlé de lui par la suite, il aurait bien pu
                    devenir Red Hood et tomber dans la cuve d'acide.</p><br/>

                    
