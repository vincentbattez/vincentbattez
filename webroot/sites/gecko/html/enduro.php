                    <p>Cette année encore, l’enduropale du Touquet s’est déroulé sous la pluie du Nord Pas de Calais, mais ce n’est pas la pluie qui fait fuir les motards qui chaque année sont plus de 1000 à se réunir pour cette course mythique où se retrouvent aussi bien des pros que des amateurs. Pour un motard cette épreuve d’endurance et de technicité est la course à faire au moins une fois dans sa vie de crossman ou d’enduriste.</p>

                    <p class="important"> <span class="ligne_important"></span>L’enduropale du Touquet, qu’est-ce que c’est ?</p>
                    
                    <p>
                        <ul>
                            <li>Une course de motocross sur la plage entre le Touquet et Stella.</li>
                            <li>3 heures de courses dans les conditions météorologiques du mois de janvier sur la côte du Touquet.</li>
                            <li>Une marée qui chaque année risque d’écourter la course.</li>
                            <li>1000 motards réunis sur la ligne de départ près à manger du sable, beaucoup de sable</li>
                        </ul>
                    </p>

                    <p>Des pilotes qui après 1 année d’attente et d’entrainement n’arrivent pas à arriver jusqu’à la ligne de départ car leur moto ne démarre pas, d’autres qui y arrivent mais qui cassent leur moteur dans la première ligne droite, d’autres à qui leur moto surchauffe après le fameux bouchon du premier virage. <br/>
                    Toutes ces choses font de cette course l’une des courses les plus difficiles à réaliser aussi bien au niveau physique, au niveau moto, et au niveau finances.</p>
                
                    <p>La course a énormément évolué depuis sa création il y a 41 ans, et quelques grands moments l’ont marquée. Les premières années le niveau des motards était beaucoup moins élevé qu’aujourd’hui, ce qui joue c’est la performance hallucinante des motos d’aujourd’hui. Il n’y a pas que ça qui a changé. <br/>
                    Le tracé de la course a également beaucoup évolué puisqu’au début les motards passaient dans les dunes, ce qui n’est plus le cas aujourd’hui. Après sur le retour de Stella plage au Touquet ils effectuaient une ligne droite interminable de 8 kilomètres.<br/>
                    Maintenant la ligne droite est fractionnée avec un « gauche droite » pour ralentir les motards. Effectivement avec l’évolution constante des motos, les meilleurs pilotes arrivent à rouler jusqu’à 170Km/h sur le sable de la ligne droite ! Imaginez rouler à 170 km/h sur le sable avec votre roue arrière qui se déplace de gauche à droite de presque 1 mètre.</p>
                    <p>Et la toute dernière nouveauté c’est le double départ. Cette année, l’organisation a décidé de faire un départ pour les 500 premiers et quelques secondes plus tard les autres participants démarrent quelques mètres derrière. Ceci a permis cette année de fluidifier énormément le départ et de causer moins d’accident.</p><br/>
                    
