                    <p>On le sait, les séries TV sont de plus en plus présentes à la télévision, pour la simple et bonne raison que le taux de production de séries est en augmentation d’années en années.</p>

                    <p class="important"> <span class="ligne_important"></span>Alors, pour vous aider à faire le tri, je vous propose un petit top des nouvelles séries américaines les plus attendues en 2016.</p>
                    <br/><br/><span class="titre">AMERICAN CRIME STORY : THE PEOPLE VS. O.J. SIMPSON</span>
                    <center><img alt="affiche d'AMERICAN CRIME STORY : THE PEOPLE VS. O.J. SIMPSON" src="../img/articles/top_series/top_series2.jpg"/></center>
                    <p>Après le succès d’American Horror Story et Scream Queens, Ryan Murphy se lance dans une nouvelle aventure avec American Crime Story. Comme les 2 séries précédentes, celle-ci est également produite sur le principe de l’anthologie : à chaque saison une histoire différente. Ainsi, cette dernière traitera de faits divers qui ont marqué l’histoire des Etats-Unis. Dans la première saison qui a débuté outre-Atlantique le 2 février, nous découvrons le procès d’O.J. Simpson, cet ancien footballeur américain qui était accusé d’avoir assassiné son ex-femme et son compagnon en 1994, un procès qui avait été très médiatisé à l’époque.
                    Nous retrouverons au casting quelques têtes déjà bien connues de la télévision ou du cinéma, à savoir Cuba Gooding Jr., acteur oscarisé, dans le rôle-titre, Sarah Paulson (American Horror Story), David Schwimmer (Friends), ou encore John Travolta.
                    </p>
                    <p>L’avis de la rédac’ : Il n’est pas toujours aisé de transposer à l’écran une affaire aussi sensible qui a fait couler beaucoup d’encre mais Ryan Murphy et son équipe ont réussi l’exercice. Peu importe que l’on connaisse déjà l’issue du verdict, la série nous permet de découvrir certains éléments que l’on ignorait jusqu’ici. La force de la série est sans aucun doute son casting 4 étoiles, avec une mention spéciale à Sarah Paulson qui nous offre une interprétation extraordinaire dans le rôle de la procureur.</p>
                    
                    <br/><br/><span class="titre">DC’S LEGENDS OF TOMMOROW</span>
                    <center><img alt="affiche d'DC’S LEGENDS OF TOMMOROW" src="../img/articles/top_series/top_series3.jpg"/></center>
                    <p>Les succès de Arrow et Flash ont convaincu la petite chaîne américaine de développer un nouveau spin-off intitulé DC’s Legends of Tommorow. Comme ses 2 séries-mères, ces nouvelles aventures de super-héros sont signées Greg Berlanti, Marc Guggenheim et Andrew Kreisberg et nous propose de suivre The A.T.O.M., White Canary, Firestorm, Hawkgirl et Hawkman. Plusieurs d’entre eux ne sont pas inconnus des fans de l’archer vert et de l’homme le plus rapide du monde puisqu’ils sont déjà tous apparus au moins une fois dans l’une des 2 séries. Ce groupe, réuni par Rip Hunter après qu’il ait eu une vision du futur, sera réuni et aura pour mission de sauver la planète.
                    </p>
                    <p>L’avis de la rédac’ : Si vous appréciez Arrow et Flash, il ne fait aucun doute que vous apprécierez cette nouvelle production et les épisodes crossovers prévus vous raviront. L’action est au rendez-vous ce qui permet aux téléspectateurs de ne pas s’ennuyer. De plus, la présence de Dominic Purcell et Wentworth Miller ne devrait certainement pas déplaire aux anciens fans de Prison Break.</p>
                    
                    <br/><br/><span class="titre">11.22.63</span>
                    <center><img alt="affiche de 11.22.63" src="../img/articles/top_series/top_series4.jpg"/></center>
                    <p>Diffusée depuis le 15 février sur la plateforme de VOD Hulu, 11.22.63 est une mini-série de 8 épisodes fantastique basée sur le bestseller du même nom de Stephen King. L’adaptation télévisée est signée J.J Abrams, le papa de Lost et Fringe, et met en scène un professeur d’anglais qui remonte dans le temps pour essayer d’éviter le meurtre de John Fritzgerald Kennedy ce qui aura de nombreuses conséquences sur l’Histoire des Etats-Unis. Le premier rôle a été confié à James Franco.
                    </p>
                    <p>L’avis de la rédac’ : Hulu propose ici sa première grande série et nous permet enfin de découvrir une adaptation réussie d’une œuvre de Stephen King. Parfois intense mais un peu inégale, 11.22.63 saura séduire les amateurs de fantastique et de science-fiction.</p>
                    
                    <br/><br/><span class="titre">THE FAMILY</span>
                    <center><img alt="affiche de THE FAMILY" src="../img/articles/top_series/top_series5.jpg"/></center>
                    <p>Diffusée sur ABC depuis le 3 Mars, The Family suit les aventures de Claire Warren, une femme politique de l’Etat du Maine, et sa famille qui voit sa vie bouleversée lorsque son fils, supposé mort depuis 10 ans, refait surface. Si elle n’a aucun doute sur l’identité du jeune homme, l’aîné de la famille reste prudent et se demande s’il est vraiment qui il prétend être. Le casting est composé de Joan Allen dans le premier rôle, Liam James (The Killing US), Allison Pill (The Newsroom) et Zach Gilford (Friday Night Lights).
                    </p>
                    <p>L’avis de la rédac’ : Au programme de The Family, de l’émotion, des mystères et un univers sombre qui font de la série un thriller passionnant. Amateurs de dramas familials, cette série est faite pour vous !</p>
                    
                    <br/><br/><span class="titre">THE CATCH</span>
                    <center><img alt="affiche de THE CATCH" src="../img/articles/top_series/top_series6.jpg"/></center>
                    <p>The Catch est la nouvelle série de Shonda Rhimes, la créatrice de Grey’s Anatomy, Scandal et How to Get Away with Murder. Autant dire que l’arrivée de cette série sur ABC est très attendue, d’autant plus que les séries de Shonda Rhimes riment le plus souvent avec succès et ça, les dirigeants d’ABC l’ont bien compris.
                    The Catch, dont la diffusion a débuté le 23 mars, c’est l‘histoire d’Alice Vaughan, expert-comptable et talentueuse enquêtrice sr les fraudes qui, alors qu’elle est sur le point de se marier, découvre que son fiancé est un escroc et que c’est elle sa dernière victime. Son unique but est désormais de le retrouver.
                    Mireille Enos (The Killing US) et Peter Krause (Six Feet Under, Parenthood), se partagent les premiers rôles.
                    </p>
                    <p>L’avis de la rédac’ : Shonda Rhimes ne change pas une formule qui gagne et qui lui a permis d’enchaîner les succès. On retrouve donc à nouveau ici une femme forte dans le rôle-titre à l’instar des 3 autres séries. Nul doute que The Catch trouvera son public, d’autant plus qu’elle sera diffusée juste après Grey’s Anatomy et Scandal. </p>
                    
